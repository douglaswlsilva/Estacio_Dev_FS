# Criação da variável texto
texto = 'Olá, laço for.'

# Laço for que itera sobre a variável texto e imprime cada caractere
for item in texto:
    print('Caractere: ' + item)

# Novo laço for que itera sobre o intervalo de 1 a 10
for numero in range(1, 11):
    print('Número do intervalo: ' + str(numero))
